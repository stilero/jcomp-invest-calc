<h1>Settings</h1>
<h2>Discount rate</h2>
<p>The rate of return that could be earned on an investment in the financial markets with similar risk. You could also use the weighted average cost of capital (WACC), ie the minimum return a company must earn to satisfy its creditors, owners etc.</p>
<p>Likely a value between 5-20, for example 12.</p>
<h2>Economic life</h2>
<p>The expected economic lifetime in years of the investment. Usually a value between 3 and 25 years, for example 5.</p>
<h2>Investment value</h2>
<p>The initial cost of the investment.</p>
<h2>Residual Value</h2>
<p>The remaining value of an assets after it has been fully depreciated, also commonly known as salvage value.</p>
<p>In other words what you could expect if you sell the investment after the period.</p>                
<h2>Payment balance</h2>
<p>The annual balance directly caused by the investment. Revenues minus costs that are a direct effect of the investment.</p>
<p>You can enter separate balances for every year, or repeat the balance of year 1 by pressing the repeat button.</p>

